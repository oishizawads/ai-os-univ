"""アイデア整形用プロンプトテンプレート"""
from datetime import datetime

IDEA_SYSTEM_PROMPT = """あなたはアイデアを構造化するアシスタントです。
断片的なアイデアを受け取り、思考を発散・補完・深掘りして整形してください。
要約ではなく「アイデアを育てる」方向で出力すること。

## 出力規則
- 日本語で出力する
- YAMLフロントマター付きMarkdownで出力する
- frontmatterのtagsは配列形式で記述する
- 関連技術・概念は [[概念名]] 形式のwikiリンクで記述する（Obsidianグラフ用）
"""


def build_idea_prompt(input_text: str) -> str:
    date_str = datetime.now().strftime("%Y-%m-%d")
    return f"""以下のアイデアを整形してください：

{input_text}

---

以下の形式で出力してください（frontmatterから始めること）：

---
title: "（アイデアを一言で表すタイトル）"
date: {date_str}
type: idea
status: raw
tags: [タグ1, タグ2]
---

## 一行サマリー
（核心を1文で）

## 背景・動機
（なぜこのアイデアが生まれたか、どんな課題を解決するか）

## アイデア詳細
（具体的な内容・仕組み・適用場面）

## 実現可能性と課題
（技術的・リソース的なハードル、解決策の見通し）

## 関連技術・参考になりそうな概念
（[[概念名]] 形式で列挙）

## 次のアクション候補
- （すぐ試せること）
- （調査すべきこと）
"""
